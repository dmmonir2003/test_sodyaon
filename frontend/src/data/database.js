// HELPER FUNCTIONS
// ============================================
function getProductById(id) {
    return exports.PRODUCTS.find((product) => product.id === id);
}
function getProductsByCategory(categoryId) {
    return exports.PRODUCTS.filter((product) => product.categoryId === categoryId);
}
function getCategoryById(id) {
    return exports.CATEGORIES.find((cat) => cat.id === id);
}
function getCategoryBySlug(slug) {
    return exports.CATEGORIES.find((cat) => cat.slug === slug);
}
function searchProducts(query) {
    const lowerQuery = query.toLowerCase();
    return exports.PRODUCTS.filter((product) => product.name.toLowerCase().includes(lowerQuery) ||
        product.bengaliName?.toLowerCase().includes(lowerQuery) ||
        product.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)));
}
function getProductsByAgeRange(ageRange) {
    return exports.PRODUCTS.filter((product) => product.ageRange?.includes(ageRange));
}
function getBestsellers() {
    return exports.PRODUCTS.filter((product) => product.bestseller === true).slice(0, 12);
}
function getNewProducts() {
    return exports.PRODUCTS.filter((product) => product.new === true).slice(0, 12);
}
function getDiscountedProducts() {
    return exports.PRODUCTS.filter((product) => product.discount && product.discount > 0)
        .sort((a, b) => (b.discount || 0) - (a.discount || 0))
        .slice(0, 12);
}
function getCategoriesByType(type) {
    return exports.CATEGORIES.filter((cat) => cat.type === type);
}
